package com.patron.factory.abstractt;
/**
 * 🏭 Patrón Abstract Factory El patrón Abstract Factory es un patrón de diseño
 * creacional que proporciona una interfaz para crear familias de objetos
 * relacionados o dependientes entre sí sin especificar sus clases concretas.
 * 
 * Este patrón permite encapsular múltiples fábricas concretas, cada una de las
 * cuales produce variantes de productos relacionados.
 * 
 * ✅ ¿Cuándo usarlo?
 * Cuando necesitas crear objetos que pertenecen a una misma familia de
 * productos (por ejemplo, UI para Windows y MacOS).
 * 
 * Cuando quieres garantizar la compatibilidad entre objetos creados por una
 * fábrica.
 * 
 * Cuando aplicas el principio de inversión de dependencias y quieres desacoplar
 * la lógica de creación.
 */
